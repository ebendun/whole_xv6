sub entry {
    my $name = shift;
    my $sysno = shift || $name;
    my $sym = "__sys_$name";
    print ".global $sym\n";
    print "$sym:\n";
    print " li a7, SYS_${sysno}\n";
    print " ecall\n";
    print " ret\n";
}
	
